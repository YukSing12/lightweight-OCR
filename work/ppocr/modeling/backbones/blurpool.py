import paddle
import numpy as np
import paddle.nn as nn
import paddle.nn.functional as F

__all__ = ['BlurPool']

class BlurPool(nn.Layer):
    def __init__(self, channels, pad_type='reflect', filt_size=4, stride=2, pad_off=0):
        super(BlurPool, self).__init__()
        self.filt_size = filt_size
        self.pad_off = pad_off
        self.pad_sizes = [int(1.*(filt_size-1)/2), int(np.ceil(1.*(filt_size-1)/2)), int(1.*(filt_size-1)/2), int(np.ceil(1.*(filt_size-1)/2))]
        self.pad_sizes = [pad_size+pad_off for pad_size in self.pad_sizes]
        self.stride = stride
        self.off = int((self.stride-1)/2.)
        self.channels = channels

        if(self.filt_size==1):
            a = np.array([1.,])
        elif(self.filt_size==2):
            a = np.array([1., 1.])
        elif(self.filt_size==3):
            a = np.array([1., 2., 1.])
        elif(self.filt_size==4):    
            a = np.array([1., 3., 3., 1.])
        elif(self.filt_size==5):    
            a = np.array([1., 4., 6., 4., 1.])
        elif(self.filt_size==6):    
            a = np.array([1., 5., 10., 10., 5., 1.])
        elif(self.filt_size==7):    
            a = np.array([1., 6., 15., 20., 15., 6., 1.])

        filt = paddle.to_tensor(a[:,None]*a[None,:],dtype='float32')
        filt = filt/paddle.sum(filt)
        self.register_buffer('filt', filt.unsqueeze([0,1]).expand((self.channels,1,4,4)), persistable=False)

        self.pad_type = pad_type

    def forward(self, inp):
        if(self.filt_size==1):
            if(self.pad_off==0):
                return inp[:,:,::self.stride,::self.stride]    
            else:
                return F.pad(inp,self.pad_sizes,self.pad_type)[:,:,::self.stride,::self.stride]
        else:
            return F.conv2d(F.pad(inp,self.pad_sizes,self.pad_type), self.filt, stride=self.stride, groups=inp.shape[1])